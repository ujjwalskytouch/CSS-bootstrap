// __________ DISPLAY - PROPERTY __________ // 

// 1. inline :- property using only required content width, or if we using this property our div or box then is not accepting any of width, height, margin, padding
// ex. span, button, input, a 
// 2. block :- property using 100% width 
// ex. div, p, h1, h2, h3, h4, h5, h6, form, ul, ol, li, table, tr, td, th, img, video, audio, iframe 
// 3. inline-block :- this property worked same as inline but we can use width, height, margin, padding 
// 4. none :- remove element & space from html


// __________ VISIBILITY : HIDDEN __________ //

// 1. This property working same as display: none but this property not remove element space from html 


// __________ POSITION - PROPERTY __________ //

// 1. static :- default position, not accepting top-bottom-left-right and z-index position
// 2. relative :- this property using his position relative to his own element
// 3. absolute :- this property using his position inside parent relative div or box 
// 4. fixed :- this property used to fix position of his element 
// 5. sticky :- this property used to scroll time fix position


// __________ FLEXBOX - PROPERTY __________ //

// 1. FLEX-DIRECTION
// ==> 1. row, column, row-reverse, column-reverse 

// 2. FLEX-WRAP : WRAP
// ==> 1. If i have 1 div-container include 4 div getting 200px width, so when my screen is smaller at a time box will be automatic wrap like 2 by 2

// 3. FLEX : shrink & grow
// ==> 1. shrink :- this property used to shrink his width 
// ==> 2. grow :- this property used to grow his width 


// __________ TRANSITION - PROPERTY __________ //

// 1. This property used to adding a hover and clicking (active) effects in element or box 


// __________ TRANSFORM - PROPERTY __________ //

// 1. rotate(45deg) :- this property used to rotate his element 
// 2. scale(2) :- this property used to increase div or box size 
// 3. translateX(100px) : Pushing move to this div on X axis

// __________ % , EM , REM __________ //

// em :- This property is used to getting his parent element size 1em === parent font size
// rem :- 1rem === root parent font size
// % :- This property follow his parent width 


